-- До этого фикса `event_type = 'like'` в user_events писался не из UI-действия,
-- а из бэкфилл-сидера `ensure_likes_recorded` при открытии своего списка
-- лайков. Все строки одного батча получали идентичный `created_at = now()`,
-- из-за чего time-decay в рекомендациях не работал (все лайки выглядели
-- "только что"). UI-лайк теперь пишется в user_events оптимистично из
-- LikesService::like_track с реальным временем — сидер удалён.
--
-- Удаляем накопленный мусор. Источник истины по составу лайков остаётся
-- `user_likes_tracks`, так что фактические лайки не теряются — теряется
-- только испорченная временная шкала, которой и не было.

DELETE FROM user_events WHERE event_type = 'like';
